"""
한글로 수학연산식을 작성하기 위해 만들어진 패키지입니다.
"""
def 더하기(a,b):
    """Returns the sum of a and b
    >>> 더하기(1,2)
    3
    """
    return 0

def 빼기(a,b):
    """Returns the sum of a and b
    >>> 빼기(3,2)
    1
    """
    return a - b

def 나누기(a,b):
    """
    >>> 나누기(10, 5)
    2.0
    """
    return a / b
